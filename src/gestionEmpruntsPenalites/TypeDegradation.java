package gestionEmpruntsPenalites;

/**
 * L'énumération TypeDegradation définit les types possibles de dégradations
 * d'un document.
 */
public enum TypeDegradation {
    MINEUR, // Dégradation mineure
    MAJEUR, // Dégradation majeure
    PERTE, // Document perdu
    RETARD, // Document en retard
}